/// Get prophecy text for a given card, topic, and orientation.
String getProphecyText({
  required int cardIndex,
  required String topicKey,
  bool isReversed = false,
}) {
  final map = isReversed ? kReversedProphecyTexts : kProphecyTexts;
  final cardMap = map[cardIndex];
  if (cardMap == null) return 'The fates are silent...';
  return cardMap[topicKey] ?? 'The fates are silent...';
}
